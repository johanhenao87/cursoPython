# Definir nombres de variables  con primera letra minuscula y las siguientes palabras siempre la  primera letra con Mayuscula
#esto es conocido como Camel Case ejemplo:

nombreCompleto = "Johan Andres Rojas Henao"

#Python recomienda usar el estilo snake_case el mejor ejemplo de esto seria:

bienvenida_usuario = "Hola " + nombreCompleto + " como estas hoy? "

#print se utiliza para imprimir en pantalla el valor o codigo deseado
print(bienvenida_usuario);  


#Operadores de pertenencia (in / not in) este es utilizado para buscar ciertos criterios dados por el usuario
# y devolver true o false como por ejemplo:

print("Hola" in bienvenida_usuario);





